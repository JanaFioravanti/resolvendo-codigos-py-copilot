#Concatenar a string com ela mesma o número de vezes informado pelo usuário.

#Entrada de dados
texto = input("Digite um texto: ")
numero = int(input("Digite um número inteiro: "))

#Processamento
resultado = (texto + " ") * numero

#Saída de dados
print(resultado)

